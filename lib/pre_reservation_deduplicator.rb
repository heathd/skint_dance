require 'digest/md5'

class PreReservationDeduplicator
  # 1. classify all reservations in to "double" or single
  # go through each category and look for later ones of in same class, delete the later ones

  def all
    PreReservation.where("created_at > ?", Time.zone.parse("2014-08-06 19:00:00 BST")).all
  end

  def doubles
    all.select {|pr| is_double?(pr) }
  end

  def singles
    all.select {|pr| ! is_double?(pr) }
  end

  def is_double?(pre_reservation)
    order_count_for_email(pre_reservation.email) == 2
  end

  def order_count_for_email(email)
    @counts ||= {}
    @counts[email] ||= order_counts.find {|e, _| e == Digest::MD5.hexdigest(email)}.last
  end

  def process_singles
    set = singles

    emails = set.map {|pr| pr.email}.uniq

    emails.each do |email|
      matching_prs = set.select {|pr| pr.email == email }

      puts matching_prs.map {|pr| "#{pr.id}: #{pr.created_at}"}
      matching_prs[1..-1].each do |pr|
        puts "destroy #{pr.id}"
        pr.destroy
      end
    end
  end


  def process_doubles
    set = doubles

    emails = set.map {|pr| pr.email}.uniq

    emails.each do |email|
      matching_prs = set.select {|pr| pr.email == email }

      puts email
      puts matching_prs.map {|pr| "#{pr.id}: #{pr.created_at}"}
      matching_prs[2..-1].each do |pr|
        puts "destroy #{pr.id}"
        pr.destroy
      end
    end
  end


